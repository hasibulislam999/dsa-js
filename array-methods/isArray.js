/* Array.isArray(array_variable) */
// determines whether the passed value is an Array or Not, returns true or false
const arr = [1, 3, 5, 7, 9, 3];
const str1 = ['JavaScript', 'Python', 'C++'];
const str2 = 'C, Java, SQL';
console.log(Array.isArray(arr));
console.log(Array.isArray(str1));
console.log(Array.isArray(str2));
